<!-- Numeros y operadores -->

<!-- 1. Operadores Aritméticos -->
<p>
    Suma (+): se utiliza para sumar dos valores. <br>
    Resta (-): se utiliza para restar un valor de otro. <br>
    Multiplicación (*): se utiliza para multiplicar dos valores. <br>
    División (/): se utiliza para dividir un valor entre otro. <br>
    Módulo (%): se utiliza para obtener el resto de una división. <br>
    Incremento (++) y decremento (--): se utilizan para aumentar o disminuir el valor de una variable en una unidad 
</p><br>

<!-- 2. Operador de asignacion = -->

<p> Asignacion (=): Asigna un valor a una variable </p><br>

<!-- 3. Operadores de arrays -->

<p>
Operador de unión (+): Combina dos arrays en uno solo, conservando las claves y los valores de ambos arrays. Si hay claves duplicadas, se conservará el valor del primer array. <br>

Operador de igualdad (==): Compara dos arrays y devuelve true si tienen los mismos pares clave/valor, sin importar el orden de los elementos. <br>

Operador de identidad (===): Compara dos arrays y devuelve true solo si tienen los mismos pares clave/valor en el mismo orden. <br>

Operador de diferencia (-): Devuelve un array que contiene los elementos del primer array que no están presentes en el segundo array.</p><br><br>

<!-- 4. Operadores bit a bit -->

<p>
AND bit a bit (&): Realiza una operación AND lógica bit a bit entre dos operandos. El resultado es 1 si ambos bits correspondientes son 1, de lo contrario es 0. <br><br>

OR bit a bit (|): Realiza una operación OR lógica bit a bit entre dos operandos. El resultado es 1 si al menos uno de los bits correspondientes es 1, de lo contrario es 0. <br><br>

XOR bit a bit (^): Realiza una operación XOR lógica bit a bit entre dos operandos. El resultado es 1 si exactamente uno de los bits correspondientes es 1, de lo contrario es 0. <br><br>

Desplazamiento a la izquierda (<<): Desplaza los bits de un operando hacia la izquierda una cantidad específica de posiciones. Cada desplazamiento hacia la izquierda duplica el valor original. <br><br>

Desplazamiento a la derecha (>>): Desplaza los bits de un operando hacia la derecha una cantidad específica de posiciones. Cada desplazamiento hacia la derecha reduce a la mitad el valor original. <br><br>

NOT bit a bit (~): Realiza una operación NOT lógica bit a bit en un operando. Invierte todos los bits, cambiando 1 por 0 y viceversa. <br><br>
</p>